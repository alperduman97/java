package day02_dataTurleri_Scanner;

public class c02_nonPrimitiveDatatürleri {

    public static void main(String[] args) {

        String str = "Java Candır";

        int sayi = 20;

        System.out.println(sayi); //20

        System.out.println(str.toUpperCase()); // JAVA CANDIR --> BÜYÜK YAZDIRIR
        System.out.println(str.toLowerCase()); // java candır --> küçük yazdırdı


        System.out.println(str); // Java Candır

          /* primitive data türleri sadece değer alırlar. Non-primitive data türleri ise değerler ile
          birlikte metodlar da barındırırlar.
           */




    }


}
